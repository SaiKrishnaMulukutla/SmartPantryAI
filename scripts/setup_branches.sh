#!/usr/bin/env bash
# ─────────────────────────────────────────────────────────────────────────────
# setup_branches.sh
# Rebuilds SmartPantryAI git history with 7 feature branches,
# multiple commits per branch, and --no-ff merges into main.
#
# Usage: bash scripts/setup_branches.sh
# ─────────────────────────────────────────────────────────────────────────────
set -e

REMOTE="https://github.com/SaiKrishnaMulukutla/SmartPantryAI.git"
ROOT="$(cd "$(dirname "$0")/.." && pwd)"
cd "$ROOT"

echo ""
echo "═══════════════════════════════════════════════════════"
echo "  SmartPantryAI — Rebuilding Git History"
echo "═══════════════════════════════════════════════════════"

# ─── Step 0: Backup all files, reinitialize repo ──────────────────────────────
echo ""
echo "[0/8] Backing up files and reinitialising repo …"

BACKUP=$(mktemp -d)
echo "  Backup dir: $BACKUP"

# Copy only source files — skip large data, models, runs, .git
SOURCE_FILES=(
    README.md .gitignore requirements.txt app.py
    data/classes.txt data/dataset.yaml data/prepare_dataset.py
    preference_engine/__init__.py preference_engine/schema.py preference_engine/preference_ui.py
    recipe_engine/__init__.py recipe_engine/prompt_builder.py recipe_engine/groq_client.py recipe_engine/recipe_parser.py
    detector/__init__.py detector/yolo_detector.py detector/frame_processor.py
    training/__init__.py training/train.py training/evaluate.py training/export.py training/kaggle_train.py
    ui/__init__.py ui/components.py ui/styles.css
    scripts/setup_branches.sh
)

for f in "${SOURCE_FILES[@]}"; do
    mkdir -p "$BACKUP/$(dirname $f)"
    cp "$ROOT/$f" "$BACKUP/$f" 2>/dev/null && echo "  backed up: $f" || echo "  MISSING:   $f"
done

# Nuke git history and reinitialize
rm -rf "$ROOT/.git"
git init
git remote add origin "$REMOTE"
git checkout -b main

# ─── Helper functions ─────────────────────────────────────────────────────────
c() {
    # c "<files>" "<commit message>"
    git add $1
    git commit -m "$2"
    echo "    + $2" | head -c 80
    echo ""
}

merge_to_main() {
    local branch="$1"
    local msg="$2"
    git push origin "$branch"
    git checkout main
    git merge --no-ff "$branch" -m "$msg"
    git branch -d "$branch"
    echo "  ✓ merged $branch → main"
}

from_backup() {
    local file="$1"
    local dir
    dir=$(dirname "$ROOT/$file")
    mkdir -p "$dir"
    if ! cp "$BACKUP/$file" "$ROOT/$file"; then
        echo "  WARNING: could not restore $file from backup"
    fi
}

# ═══════════════════════════════════════════════════════
# INITIAL COMMIT — main
# ═══════════════════════════════════════════════════════
echo ""
echo "[base] Initial commit on main …"
from_backup "README.md"
from_backup ".gitignore"

c "README.md .gitignore" \
"chore: initial project scaffold

Add project README (SmartPantryAI) and .gitignore for
Python/ML project — excludes .env, models/, dataset images."


