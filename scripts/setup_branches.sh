# BRANCH 1: feat/project-setup
# ═══════════════════════════════════════════════════════
echo ""
echo "[1/8] feat/project-setup …"
git checkout -b feat/project-setup

from_backup "requirements.txt"
c "requirements.txt" \
"chore: add requirements.txt with core dependencies

Pin ultralytics, streamlit, groq, pydantic, opencv-python,
python-dotenv, roboflow, kaggle, pyyaml. Tested on Python 3.13
Apple Silicon (M1 Pro)."

cat > "$ROOT/.env.example" << 'EOF'
GROQ_API_KEY=your_groq_api_key_here
ROBOFLOW_API_KEY=your_roboflow_api_key_here
KAGGLE_API_TOKEN=KGAT_your_token_here
KAGGLE_USERNAME=your_kaggle_username
YOLO_MODEL_PATH=models/food_yolo11/best.pt
CONFIDENCE_THRESHOLD=0.5
MAX_RECIPES=3
EOF
git add -f .env.example
git commit -m \
"chore: add .env.example documenting all required env vars

Covers Groq, Roboflow, Kaggle credentials and app config defaults.
Never commit the actual .env — it is gitignored."

merge_to_main "feat/project-setup" \
"feat: project setup — requirements and env config (#1)"


